# 1 count - показывается сколько раз повторяется указанное значение 


numbers = (1, 3, 7, 8, 7, 5, 4, 6, 8, 5, 5)

x =numbers.count(5)

print(x)



# 2 index - показывает под каким индексом находится указанное вами значение (если значения нет то он вызавает исключение) 

numbers_2 = (1, 3, 7, 8, 7, 5, 4, 6, 8, 5)

x = numbers_2.index(8)

print(x)